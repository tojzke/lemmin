#include "includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t us1;

	i = 0;
	us1 = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[us1 + i] = s2[i];
		i++;
	}
	s1[us1 + i] = '\0';
	return (s1);
}

#include "includes/libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if ((s != NULL) && (f != NULL))
	{
		while (*s != 0)
		{
			f(s);
			s++;
		}
	}
}
